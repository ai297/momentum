function getWeatherIcon(iconName) {
    if(WEATHER_ICONS.hasOwnProperty(iconName)) 
        return `<svg xmlns="http://www.w3.org/2000/svg" viewBox="0 0 91 91" fill="currentColor">${WEATHER_ICONS[iconName]}</svg>`;
}
function parseWeatherId(id, isNight = false) {
    switch(true) {
        case id >= 210 && id <= 221:
            return getWeatherIcon('thunderStorm');
        case id >= 200 && id <= 232:
            return getWeatherIcon('thunderStormWithRain');
        case id >= 300 && id < 310:
            return getWeatherIcon('drizzle');    
        case id >= 310 && id <= 321:
        case id >= 520 && id <= 531:
            return getWeatherIcon('rain');
        case id >= 500 && id <= 504:
            return getWeatherIcon(`rain_${ isNight ? 'n' : 'd' }`);
        case id == 511:
            return getWeatherIcon('hail');
        case id == 600 || id == 601 || id == 611 || id == 620:
            return getWeatherIcon(`snow_${ isNight ? 'n' : 'd' }`);
        case id >= 600 && id <= 622:
            return getWeatherIcon('snow');
        case id >= 701 && id <= 731:
            return getWeatherIcon('mist');
        case id >= 741 && id <= 771:
            return getWeatherIcon('fog');
        case id == 781:
            return getWeatherIcon('tornado');
        case id == 801:
            return getWeatherIcon(`fewClouds_${ isNight ? 'n' : 'd' }`);
        case id == 802:
            return getWeatherIcon('scatteredClouds');
        case id >= 803:
            return getWeatherIcon('brokenClouds');
        case id == 800:
        default:
            return getWeatherIcon(`clear_${ isNight ? 'n' : 'd' }`);
    }
}
let trackInfoTimeOut;
function loadTrackInfo(elem) {
    fetch('https://myradio24.com/users/cantina/status.json').then(res => {
        if (!res.ok) throw Error(res.statusText);
        res.json().then(result => {
            elem.innerHTML = `
                <p class="description">DJ: ${result.djname}</p>
                <span>${result.song}</span>
            `;
            trackInfoTimeOut = setTimeout(() => loadTrackInfo(elem), 10000);
        })
    })
    .catch(err => {
        elem.innerHTML='';
        console.log(err);
    })
}

window.onload = () => {
    if(localStorage.getItem('isVisited') !== 'visited') {
        document.getElementById('cross-check-intro').style.display='flex';
    }

    const timeElement = document.getElementById('time');
    const dateElement = document.getElementById('date');
    const greetingElement = document.getElementById('greeting');
    const userName = document.getElementById('user-name');
    const dayFocus = document.getElementById('day-focus');
    const userCity = document.getElementById('user-city');
    const backImageElement = document.querySelector('.back-image');
    const quoteElement = document.getElementById('random-quote');
    const weatherWidget = document.querySelector('.weather-widget');
    const sunTimes = document.querySelector('.sun-time');

    const clock = new Clock(timeElement, dateElement);
    const app = new Momentum(IMAGES);

    let changeImageDisable = false;
    let changeQuoteDisable = false;

    // select active slider element
    let activeSlider = localStorage.getItem('selected-element') || 1;
    document.querySelector(`.slider > .slider-item:nth-child(${activeSlider})`)
        .classList.add('active');
    document.querySelector(`[data-slider="${activeSlider}"]`).classList.add('active');

    // change quote
    function nextQuote() {
        if(changeQuoteDisable) return;
        
        changeQuoteDisable = true;
        quoteElement.style.opacity = 0;

        app.getQuote().then(result => {
            setTimeout(() => {
                quoteElement.innerHTML = `<span>${result.quote}</span><div>${result.author}</div>`;
                changeQuoteDisable = false;
            }, 500);
            setTimeout(() => quoteElement.style.opacity = 1, 500);
        });    
    }

    function initSlider() {
        document.querySelectorAll('[data-slider]').forEach(el => {
            el.addEventListener('click', () => {
                document.querySelector(`[data-slider="${activeSlider}"]`)
                    .classList.remove('active');
                el.classList.add('active');

                activeSlider = el.getAttribute('data-slider');
                localStorage.setItem('selected-element', activeSlider);

                const activeElement = document.querySelector('.slider > .slider-item.active');
                const nextElement = document.querySelector(`.slider > .slider-item:nth-child(${activeSlider})`);
                
                if(activeElement == nextElement) return;
                activeElement.classList.add('out');
                activeElement.classList.remove('active');
                nextElement.classList.add('active');
                
                setTimeout(() => {
                    activeElement.classList.remove('out');
                }, 500);
            })
        });
    }

    function loadWeather() {
        const fixZero = n => n < 10 ? '0' + n : n;
        sunTimes.style.opacity = 0;
        if(app.userCity == app.cityPlaceholder) {
            weatherWidget.innerHTML = '<span>City does not set</span>';
            return;
        }

        weatherWidget.innerHTML = '<span>Load...</span>';

        app.getWeather()
        .then(res => {
            weatherWidget.style.opacity = 0;
            setTimeout(() => {
                const sunRiseTime = new Date(res.sys.sunrise * 1000);
                const sunSetTime = new Date(res.sys.sunset * 1000);
                sunTimes.innerHTML=`<div>${getWeatherIcon('sunrise')}<span>${sunRiseTime.getHours()}:${fixZero(sunRiseTime.getMinutes())}</span></div>
                                    <div>${getWeatherIcon('sunset')}<span>${sunSetTime.getHours()}:${fixZero(sunSetTime.getMinutes())}</span></div>`;
                sunTimes.style.opacity = 1;
                const hours = new Date().getHours();
                weatherWidget.innerHTML = `
                    <div class="compas" style="transform: rotate(${res.wind.deg}deg);">${getWeatherIcon('compas')}</div>
                    <div class="wind-and-humidity">
                        <div class="wind">
                            ${getWeatherIcon('wind')}
                            <span>${Math.round(res.wind.speed*10)/10} m/s</span>
                        </div>
                        <div class="humidity">
                            ${getWeatherIcon('wet')}
                            <span>${res.main.humidity}%</span>
                        </div>
                    </div>
                    <div class="icon">
                        ${parseWeatherId(res.weather[0].id, (hours >= 23 || hours < 6))}
                        <p class="description">
                            ${res.weather[0].description}
                        </p>
                    </div>
                    <div>
                        <div class="temperature">
                            ${getWeatherIcon('thermometer')}
                            <span>${res.main.temp > 0 ? '+' : ''}${Math.round(res.main.temp)}</span>
                            <span>°С</span>
                        </div>
                        <div class="description">
                            Feels like ${res.main.feels_like > 0 ? '+': ''}${Math.round(res.main.feels_like)}°.
                        </div>
                    </div>`;
                    weatherWidget.style.opacity = 1;
            }, 500);
        })
        .catch(err => {
            weatherWidget.style.opacity = 0;
            console.log("error", err);
            setTimeout(() => {    
                weatherWidget.innerHTML = '<span>Weather loading fail :(</span>';
                weatherWidget.style.opacity = 1;
            }, 500);
        });
    }

    function appStart() {
        // кнопка смены картинки
        const changeImageButton = document.getElementById('change-image');
        changeImageButton.addEventListener('click', (e) => {
            if(!changeImageDisable) {
                app.changeImage(backImageElement);
                changeImageDisable = true;
                changeImageButton.disabled = true;
                setTimeout(() => {
                    changeImageDisable = false;
                    changeImageButton.disabled = false;
                }, 1000);
            }
        });

        // кнопка смены цитаты
        const quoteButton = document.getElementById('change-quote');
        quoteButton.addEventListener('click', el => {
            if(quoteButton.getAttribute('data-slider') == activeSlider) nextQuote();
        });
        
        // слайдер
        initSlider();

        clock.showTime();
        nextQuote();
    }


    userName.innerHTML = app.userName;
    dayFocus.innerHTML = app.dayFocus;
    userCity.innerHTML = app.userCity;

    userName.onblur = function() {
        if(userName.innerText != '') app.userName = userName.innerText;
        userName.innerHTML = app.userName;
        greetingElement.innerHTML = app.getGreeting(clock.hours);
    }
    dayFocus.onblur = function() {
        if(dayFocus.innerText != '') app.dayFocus = dayFocus.innerText;
        dayFocus.innerHTML = app.dayFocus;
    }
    userCity.onblur = function() {
        if(userCity.innerText != '') app.userCity = userCity.innerText;
        userCity.innerHTML = app.userCity;
        loadWeather();
    }

    // обработка клика по полю ввода, нажатия enter и попытки использовать Ctrl+V
    document.querySelectorAll('.text-field').forEach(elem => {
        elem.addEventListener('focus', () => {
            elem.innerHTML = '';
        });
        elem.addEventListener('keypress', e => {
            if(e.keyCode == 13) {
                e.preventDefault();
                elem.blur();
            }
        });
        elem.addEventListener('paste', e => {
            let paste = (e.clipboardData || window.clipboardData).getData('text');
            const selection = window.getSelection();
            if (!selection.rangeCount) return false;
            selection.deleteFromDocument();
            selection.getRangeAt(0).insertNode(document.createTextNode(paste));
            e.preventDefault();
        });
    });

    // новый час на часах
    document.addEventListener('change-hour', function (e) {
        greetingElement.innerHTML = app.getGreeting(e.detail);
        dayFocus.innerHTML = app.dayFocus;
        app.changeImage(backImageElement, e.detail);
        loadWeather();
    });

    // предварительная загрузка изображений
    document.addEventListener('image-loading', function (e) {
        document.getElementById('loading').innerText = e.detail + '%';
        // когда все изображения загружены
        if(e.detail > 99) {
            
            appStart();

            setTimeout(
                () => document.getElementById('preloader').classList.add('hide'),
                300);
        }
    });

    document.getElementById('start').addEventListener('click', () => {
        document.getElementById('cross-check-intro').classList.add('hide');
        localStorage.setItem( 'isVisited', 'visited');
    });

    // RADIO
    const audioPlayer = document.getElementById('radio-player');
    const radioButton = document.getElementById('radio-button');
    const radioInfoElement = document.getElementById('radio-info');
    radioButton.addEventListener('change', () => {
        if(!radioButton.checked) {
            audioPlayer.volume = 0.8;
            audioPlayer.load();
            audioPlayer.play();
            loadTrackInfo(radioInfoElement);
        }
        else {
            audioPlayer.pause();
            clearTimeout(trackInfoTimeOut);
            radioInfoElement.innerHTML = '';
        }
        radioButton.disabled = true;
        setTimeout(() => radioButton.disabled = false, 1000);
    });
};